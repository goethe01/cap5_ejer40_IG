    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // codigo boton btnConfirmar:
        int num,cantidad;
        double raiz, cuadrado, cubo;
        String texto = "";
        
        Operaciones operacion=new Operaciones();
        
        cantidad = Integer.parseInt(txtCantidadN.getText());
        for(int i =0;i<cantidad;i++){
            String entrada = JOptionPane.showInputDialog("Ingrese un nÃºmero entero:");
            num = Integer.parseInt(entrada);
            
            raiz = operacion.setRaiz(num);
            cuadrado = operacion.setCuadrado(num);
            cubo = operacion.setCubo(num);
            
            texto = operacion.setImprimir(texto, num, raiz, cuadrado, cubo);
        }
        txtSalida.setText(texto);
        
    }//GEN-LAST:event_btnConfirmarActionPerformed

